#include<iostream>
using namespace std;
int main()
{
    int a; // This is a variable declaration. We are declaring a variable named 'a' of type 'int' (integer).
    a = 10; // This is an assignment statement. We are assigning the value 10 to the variable 'a'.
    
    cout << "The value of a is: " << a << endl; // This will print the value of 'a' to the console.
    
    float b = 5.5; // This is a variable declaration and initialization. We are declaring a variable named 'b' of type 'float' and initializing it with the value 5.5.
    
    cout << "The value of b is: " << b << endl; // This will print the value of 'b' to the console.

    bool flag = true; // This is a variable declaration and initialization. We are declaring a variable named 'flag' of type 'bool' (boolean) and initializing it with the value 'true'.

    cout << "The value of flag is: " << flag << endl; // This will print the value of 'flag' to the console.

    return 0; // This indicates that the program ended successfully.
}