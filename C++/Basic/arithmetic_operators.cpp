#include <iostream>
using namespace std;
int main()
{
    int x = 7, y = 2;
    cout << x + y << endl; // This will print the result of the addition of x and y, which is 9.
    cout << x - y << endl; // This will print the result of the subtraction of y from x, which is 5.
    cout << x * y << endl; // This will print the result of the multiplication of x and y, which is 14.
    cout << x / y << endl; // This will print the result of the division of x by y, which is 3 (integer division).
    cout << x % y << endl; // This will print the result of the modulus operation, which gives the remainder of the division of x by y, which is 1.
    return 0;
}